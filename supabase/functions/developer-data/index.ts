// Verify developer token (simple base64 JSON, not JWT)
// Token format: btoa(JSON.stringify({ developer_id, email, is_developer, exp }))
async function verifyDeveloperToken(token: string, supabase: any): Promise<{ valid: boolean; developerId?: string; reason?: string }> {
  try {
    console.log('üîê Verificando token...');
    console.log('   Token length:', token?.length);
    
    // Token is simple base64(JSON) - decode directly (no signature)
    let payload;
    try {
      payload = JSON.parse(atob(token));
      console.log('   Payload parsed:', JSON.stringify(payload, null, 2));
    } catch (parseErr) {
      console.log('‚ùå Erro ao parsear token base64:', parseErr);
      return { valid: false, reason: 'Token mal formatado' };
    }
    
    // Check is_developer flag (not "type")
    if (!payload.is_developer) {
      console.log('‚ùå Token n√£o √© de developer (is_developer =', payload.is_developer, ')');
      return { valid: false, reason: 'Token n√£o √© de desenvolvedor' };
    }
    
    // Check developer_id (with underscore, not camelCase)
    if (!payload.developer_id) {
      console.log('‚ùå developer_id n√£o encontrado no payload');
      return { valid: false, reason: 'developer_id ausente' };
    }
    
    // Check expiration
    if (payload.exp && Date.now() > payload.exp) {
      console.log('‚ùå Token expirado. Exp:', new Date(payload.exp).toISOString(), 'Now:', new Date().toISOString());
      return { valid: false, reason: 'Token expirado' };
    }
    
    // Verify developer exists in database
    console.log('üîç Buscando developer no banco:', payload.developer_id);
    const { data: developer, error: devError } = await supabase
      .from('developer_auth')
      .select('id, email')
      .eq('id', payload.developer_id)
      .single();
    
    if (devError) {
      console.log('‚ùå Erro ao buscar developer:', devError);
      return { valid: false, reason: 'Erro ao buscar developer' };
    }
    
    if (!developer) {
      console.log('‚ùå Developer n√£o encontrado no banco');
      return { valid: false, reason: 'Developer n√£o encontrado' };
    }
    
    console.log('‚úÖ Token v√°lido! Developer:', developer.email);
    return { valid: true, developerId: payload.developer_id };
  } catch (err) {
    console.log('‚ùå Erro geral na verifica√ß√£o:', err);
    return { valid: false, reason: 'Erro geral: ' + String(err) };
  }
}

